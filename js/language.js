const textElements = document.querySelectorAll('[data-i18n]');
const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
const toggleButton = document.getElementById('languageToggle');

function applyTranslations(lang) {
  document.documentElement.lang = lang;
  textElements.forEach((el) => {
    const key = el.dataset.i18n;
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  placeholderElements.forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });
  if (toggleButton) {
    toggleButton.textContent = lang === 'en' ? 'TR' : 'EN';
    toggleButton.setAttribute('aria-label', lang === 'en' ? "Türkçe'ye geç" : 'Switch to English');
  }
  localStorage.setItem('uniqueStylistLang', lang);
}

const savedLang = localStorage.getItem('uniqueStylistLang') || 'en';
applyTranslations(savedLang);

if (toggleButton) {
  toggleButton.addEventListener('click', () => {
    const newLang = document.documentElement.lang === 'en' ? 'tr' : 'en';
    applyTranslations(newLang);
  });
}
